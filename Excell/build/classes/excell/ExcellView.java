    /**
     * Sets up 3 new sheets (sheet1, sheet2, sheet3)
     * Sets the first sheet as the current working sheet
     */
    
    public void sheetSetup()
    {
        JPanel panel;
        for(int y=0; y < 3; y++)
	{
            if(Node.insert("Sheet"+(y+1),false,"")==false)
            {
                panel = new JPanel();
                jTabbedPane1.addTab("Sheet"+(y+1), panel);
                jTextArea1.append("Sucessfully added sheet " + "Sheet"+(y+1) + "\n");
                Node.setSelectedSheet(Node.getHeadNode());
                this.jTabbedPane1.setSelectedIndex(0);
            }
	    else
                jTextArea1.append("Duplicate " + "Sheet"+(y+1) + " detected" + "\n");
	}
		
        this.jTabbedPane1.setSelectedIndex(0);
        Node.setSelectedSheet(Node.getHeadNode());   
        
        this.populateCombos();
        this.ComboBoxesInitlialState();
    }
    
    
    /**
     * Redraws the sheet tabs when a change to the sheet linked list takes place
     */
    
    public void reDrawTabs()
    {
        this.jTabbedPane1.removeAll();
        JPanel panel;
        
        Node iter = Node.getHeadNode();
    
        while(iter!=null)
        {
            panel = new JPanel();
            jTabbedPane1.addTab(iter.getNodeValue(), panel);
            iter = iter.getNextNode();
        }       
    }
    
    
    /**
     * Updates the combo boxes with the linked list sheet names
     */
    
    public void populateCombos()
    {
        this.jComboBox1.removeAllItems();
        this.jComboBox2.removeAllItems();
        this.jComboBox3.removeAllItems();
        this.jComboBox4.removeAllItems();
        this.jComboBox5.removeAllItems();
        
        Node iter = Node.getHeadNode();
        while(iter!=null)
        {
            this.jComboBox1.addItem(iter.getNodeValue());
            this.jComboBox2.addItem(iter.getNodeValue());
            this.jComboBox3.addItem(iter.getNodeValue());
            this.jComboBox4.addItem(iter.getNodeValue());
            this.jComboBox5.addItem(iter.getNodeValue());
            iter = iter.getNextNode();
        }
    }    
    
    
