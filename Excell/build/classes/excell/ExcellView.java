/**
 * Inserts a new sheet into the linked list
 * @param evt
 */

private void jButton1MouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jButton1MouseClicked
    String newSheet = this.jTextField1.getText();
    boolean beforeOrAfter;
    
    if(this.jCheckBox3.isSelected()==true)
    {
        beforeOrAfter = false;
    }
    else
    {
        beforeOrAfter = true;
    }
    
    if(Node.insert(newSheet, beforeOrAfter, this.jComboBox4.getSelectedItem().toString())==false)
    {
        int index = Node.Search(newSheet);
        Node node = Node.get(index);
        this.jTextArea1.append("\nAdded " + newSheet + " (" + node +")\n");
        this.reDrawTabs();
        this.populateCombos();
        Node.setSelectedSheet(Node.getHeadNode());
        this.jTabbedPane1.setSelectedIndex(0);
    }
    else
    {
        int index = Node.Search(newSheet);
        Node node = Node.get(index);
        this.jTextArea1.append("\nSheet " + newSheet + " (" + node +") already exists\n");
    }
}//GEN-LAST:event_jButton1MouseClicked


/**
 * Updates the slected sheet when a tabbed pane sheet is selected
 * @param evt
 */

private void jTabbedPane1MouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jTabbedPane1MouseClicked
    int item = this.jTabbedPane1.getSelectedIndex();
    String name = this.jTabbedPane1.getTitleAt(item);
    
    Node iter = Node.getHeadNode();
    
    while(iter!=null)
    {
        if(iter.getNodeValue().equalsIgnoreCase(name))
        {
            Node.setSelectedSheet(iter);
            this.jTextArea1.append("Selected sheet : " + iter.getNodeValue() + "\n");
        }
        iter = iter.getNextNode();
    }
    
}//GEN-LAST:event_jTabbedPane1MouseClicked


/**
 * Renames a sheet in the linked list
 * @param evt
 */

private void jButton2MouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jButton2MouseClicked
    String targetItem = this.jComboBox5.getSelectedItem().toString();
    String newName = this.jTextField2.getText();
    
    if(Node.rename(targetItem, newName)==true)
    {
        this.jTextArea1.append("\nRenamed " + targetItem + " to " + newName + "\n");
        this.reDrawTabs();
        this.populateCombos();
        Node.setSelectedSheet(Node.getHeadNode());
        this.jTabbedPane1.setSelectedIndex(0);
    }
    else
    {
        this.jTextArea1.append("\nUnable to rename " + targetItem + " to " + newName + " : Sheet name already exists\n");
    }
    
}//GEN-LAST:event_jButton2MouseClicked


/**
 * Moves a sheet in the linked list
 * @param evt
 */

private void jButton8MouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jButton8MouseClicked
    String item = this.jComboBox1.getSelectedItem().toString();
    String target = this.jComboBox2.getSelectedItem().toString();
    
    boolean beforeOrAfter;
    
    if(this.jCheckBox1.isSelected()==true)
    {
        beforeOrAfter = false;
    }
    else
    {
        beforeOrAfter = true;
    }
    
    if(Node.move(item, beforeOrAfter, target)==true)
    {
        this.jTextArea1.append("\nSuccessfully moved " + item + "\n");
        this.reDrawTabs();
        this.populateCombos();
        Node.setSelectedSheet(Node.getHeadNode());
        this.jTabbedPane1.setSelectedIndex(0);
    }
    else
    {
        this.jTextArea1.append("\nUnable to move " + item + " to " + target + " : Destination is the same as origin\n");
    }
    
}//GEN-LAST:event_jButton8MouseClicked


/**
 * Removes a sheet in the linked list
 * @param evt
 */

private void jButton9MouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jButton9MouseClicked
    String item = this.jComboBox3.getSelectedItem().toString();
    int pos = Node.Search(item);
    Node node = Node.get(pos);
    
    if(Node.remove(node)==true)
    {
        this.jTextArea1.append("\nSuccessfully deleted " + item + "\n");
        this.reDrawTabs();
        this.populateCombos();
    }
    else
    {
        this.jTextArea1.append("\nOnly one sheet left, ignoring operation \n");
    }
    
}//GEN-LAST:event_jButton9MouseClicked

